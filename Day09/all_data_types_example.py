# Variable Definitions
name = "Ambar"                     # str
age = 22                           # int
height = 5.9                       # float
is_student = True                 # bool
colors = ["red", "blue", "green"] # list
coordinates = (10, 20)            # tuple
user = {"name": "Ambar", "age": 22}  # dict
unique_numbers = {1, 2, 3}        # set

# Print value and type of each variable
print("name:", name, "| type:", type(name))
print("age:", age, "| type:", type(age))
print("height:", height, "| type:", type(height))
print("is_student:", is_student, "| type:", type(is_student))
print("colors:", colors, "| type:", type(colors))
print("coordinates:", coordinates, "| type:", type(coordinates))
print("user:", user, "| type:", type(user))
print("unique_numbers:", unique_numbers, "| type:", type(unique_numbers))
